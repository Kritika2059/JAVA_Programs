public class java_24_array {
    public static void main(String[] args) {
        int num[]=new int[5];
        num[0] = 5;
        num[1] = 2;
        num[2] = 9;
        num[3] = 8;
        num[4] = 7;
        for (int i=0; i<5; i++) {
            System.out.println(num[i]);
        }
    }
}



public class java_10_increment_decrements {
    public static void main(String[] args) {

    //increment and decrement operators
    int i = 56;
    int j = 10;
    int k = 20;
    int a = j++; //first value of j is assigned to a then incrementes
    int b = ++k; //first k is incremented then assigned to b
    System.out.println(i++);
    System.out.println(i);
    System.out.println(); //to print blank line
    System.out.println(++i);
    System.out.println(i);
    System.out.println();
    System.out.println(a);
    System.out.println();
    System.out.println(b);
    }
}
